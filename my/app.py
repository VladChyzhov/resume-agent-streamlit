    doc = st.file_uploader("–ó–∞–≥—Ä—É–∑–∏—Ç–µ –¥–æ–∫—É–º–µ–Ω—Ç", key="edu_upload")
    if st.button("–°–æ—Ö—Ä–∞–Ω–∏—Ç—å –¥–æ–∫—É–º–µ–Ω—Ç", key="edu_save") and doc:
        db.save_document(doc)
